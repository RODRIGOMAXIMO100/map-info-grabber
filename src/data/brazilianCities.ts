// Função para obter cidades de um estado ordenadas por população
export function getCitiesByState(state: string, limit?: number): CityData[] {
  const cities = CITIES_BY_STATE[state] || [];
  const sorted = [...cities].sort((a, b) => b.population - a.population);
  return limit ? sorted.slice(0, limit) : sorted;
}

// Função para obter total de cidades disponíveis por estado
export function getTotalCitiesForState(state: string): number {
  return CITIES_BY_STATE[state]?.length || 0;
}

// Formatar população para exibição
export function formatPopulation(pop: number): string {
  if (pop >= 1000000) {
    return `${(pop / 1000000).toFixed(1)}M`;
  }
  if (pop >= 1000) {
    return `${(pop / 1000).toFixed(0)}K`;
  }
  return pop.toString();
}
